export function getSecondaryLanguage(lang) {
  return lang === "zh" ? "en" : "zh";
}

export function getText(key, lang) {
  const dictionary = I18N[lang];
  if (!dictionary) {
    return "";
  }
  return dictionary[key] || "";
}

export function formatBilingual(primaryText, secondaryText, primaryLang = DEFAULT_LANGUAGE) {
  const secondaryLang = getSecondaryLanguage(primaryLang);
  return `<span class="lang-primary" lang="${primaryLang}">${primaryText}</span><span class="lang-secondary" lang="${secondaryLang}">${secondaryText}</span>`;
}

export function loadLanguage() {
  try {
    const stored = localStorage.getItem(LANG_STORAGE_KEY);
    return stored === "zh" ? "zh" : DEFAULT_LANGUAGE;
  } catch (error) {
    return DEFAULT_LANGUAGE;
  }
}

export function saveLanguage(lang) {
  try {
    localStorage.setItem(LANG_STORAGE_KEY, lang);
  } catch (error) {
    // Ignore storage errors for language preference.
  }
}
